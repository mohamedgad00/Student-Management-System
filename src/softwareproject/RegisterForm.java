    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed

        String url = "jdbc:mysql://localhost:3306/studentdb";
        String user = "root";
        String password = "";

        // Get What is written
        String fname = fnametxt.getText();
        String lname = lnametxt.getText();
        int age = Integer.parseInt(agetxt.getText());
        String phone = phonetxt.getText();
        String address = addresstxt.getText();
        String gender;
        if (maleRadioButton.isSelected()) {
            gender = "Male";
        } else if (femaleRadioButton.isSelected()) {
            gender = "Female";
        } else {
            // No gender selected 
            JOptionPane.showMessageDialog(this, "Please select a gender");
            return;
        }
        String username = usernametxt.getText();
        String pass = passwordtxt.getText();

        // start sending data to the mysql DB
        try {
            Connection conn = DriverManager.getConnection(url, user, password);
            // Get next available ID
            // int nextId = getNextAvailableId(conn);

            // SQL insert
            String sql = "INSERT INTO student (fname, lname, age, phone, address, gender, username, password) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pstmt = conn.prepareStatement(sql);

            pstmt.setString(1, fname);
            pstmt.setString(2, lname);
            pstmt.setInt(3, age);
            pstmt.setString(4, phone);
            pstmt.setString(5, address);
            pstmt.setString(6, gender);
            pstmt.setString(7, username);
            pstmt.setString(8, pass);

            // Encrypt Passwords
            String hashedPass = hashPassword(pass);
            pstmt.setString(8, hashedPass);

            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Registered Successfully!");

            // Clear the form fields after success
            clearForm();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Registration Failed: " + ex.getMessage());
        }
    }//GEN-LAST:event_registerBtnActionPerformed

