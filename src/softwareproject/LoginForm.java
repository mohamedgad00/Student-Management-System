    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed

        String username = usernameText.getText();
        String password = passwordText.getText();

        // Connect to DB
        String url = "jdbc:mysql://localhost:3306/studentdb";
        String dbUser = "root"; // Your MySQL username
        String dbPassword = ""; // Your MySQL password (empty if using XAMPP)

        try {
            Connection conn = DriverManager.getConnection(url, dbUser, dbPassword);

            String sql = "SELECT * FROM student WHERE username = ? AND password = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);

            // Hash the entered password
            String hashedPass = hashPassword(password);

            pstmt.setString(1, username);
            pstmt.setString(2, hashedPass);

            ResultSet rs = pstmt.executeQuery();

            if (rs.next()) {
                // ‚úÖ Username and password are correct
                JOptionPane.showMessageDialog(this, "Login Successful!,");

                // Get the username
                String uname = usernameText.getText();

                // üöÄ Open HomePage
                HomePage home = new HomePage(uname);
                home.setVisible(true);

                // üö™ Close (hide) LoginForm
                this.dispose(); // closes the current login form completely

            } else {
                // ‚ùå Username or password incorrect
                JOptionPane.showMessageDialog(this, "Invalid username or password, Please Register First !!");
                RegisterForm register = new RegisterForm();
                register.setVisible(true);
                this.dispose();
            }

            conn.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_loginBtnActionPerformed

    public static String hashPassword(String password) {
        try {
            MessageDigest md = MessageDigest.getInstance("SHA-256");
            byte[] hashBytes = md.digest(password.getBytes());
            StringBuilder sb = new StringBuilder();
            for (byte b : hashBytes) {
                sb.append(String.format("%02x", b));
            }
            return sb.toString();
        } catch (NoSuchAlgorithmException e) {
            throw new RuntimeException("Hashing error", e);
        }
    }

